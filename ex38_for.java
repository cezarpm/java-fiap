public class ex37_doWhile {

    public static void main(String[] args) {
       // Exibir a soma de 1 e 100

         // Variáveis
            int i = 1;
            int soma = 0;

            // Processamento
           for (i = 1; i <= 100; i++){
            soma = soma + i;
           }

           
           System.out.println("A soma de 1 a 100 é: " + soma);
    }
}
